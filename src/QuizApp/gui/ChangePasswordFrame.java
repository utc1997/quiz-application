    private void btnUpdatePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePasswordActionPerformed
        // TODO add your handling code here:
        int response=validateInputs();
        if(response==0){
            JOptionPane.showMessageDialog(null, "please fill all details!","carefull!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(response==-2){
            JOptionPane.showMessageDialog(null, "please length should be atleast 4","carefull!",JOptionPane.ERROR_MESSAGE);
            return;

        }
        if(response==-1){
            JOptionPane.showMessageDialog(null, "password must be same","RE-TYPE PASSWORD",JOptionPane.ERROR_MESSAGE);
            return;

        }

        else if(response==1){
            try{
                userid=txtUserId.getText();
                char []password=txtPwd.getPassword();
                pwd=String.valueOf(password);
                Users u=new Users();
                u.setUserid(userid);
                u.setPassword(pwd);
                u.setUsertype("student");
                boolean regsuccess=UsersDAO.changeStudentPassword(u);
                if(regsuccess==true){
                    JOptionPane.showMessageDialog(null, "STUDENT REGISTRATION SUCCESSFULL","REGISTERED",JOptionPane.INFORMATION_MESSAGE);
                    clearAll();
                }
                else
                JOptionPane.showMessageDialog(null, "STUDENT REGISTRATION unSUCCESSFULL or you cannot change ur username","unREGISTERED",JOptionPane.ERROR_MESSAGE);
                StudentScreenFrame studentScreenFrame= new StudentScreenFrame();
                studentScreenFrame.setVisible(true);
                this.dispose();

            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "could not connectd to database"+ex,"SQL EXCEPTION!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();

            }

        }
    }//GEN-LAST:event_btnUpdatePasswordActionPerformed

