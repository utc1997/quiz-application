    private int validateInputs() {
       userid=txtUserId.getText();
       char []password=txtPwd.getPassword();
       char []checkPassword=txtPwdCheck.getPassword();
       pwd=String.valueOf(password);
       checkPwd=String.valueOf(checkPassword);
       
       if(userid.isEmpty()|| password.length==0 ||checkPassword.length==0 )
            return 0;
       if(userid.length()<4||checkPassword.length<4)
           return -2;
       if(pwd.equals(checkPwd))
          return 1;
       else
           return -1;
    }

   
